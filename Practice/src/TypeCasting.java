
public class TypeCasting {

	public static void main(String[] args) {
			
		int x1=10;
		double y1 = x1;
		System.out.println(y1);
		System.out.println("*********************************************");
		
		double x2 = 10.5;
		int y2 = (int) x2;
		System.out.println(y2);
		System.out.println("*********************************************");
		
		double x3 = 10.5;
		int y3 =(int) x3;
		System.out.println(y3);
		System.out.println("*********************************************");
	}

}
